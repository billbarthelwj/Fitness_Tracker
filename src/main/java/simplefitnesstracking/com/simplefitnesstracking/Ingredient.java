package simplefitnesstracking.com.simplefitnesstracking;

public class Ingredient {
    private String id;
    private String name;
    private String description;
    private String notes;
    private String url;
    private double numberOfContainers;
    private double servingsPerContainer;
    private double gramsPerServing;
    private double caloriesPerServing;
    private double totalFatPerServing;
    private double saturatedFatPerServing;
    private double transFatPerServing;
    private double cholesterolPerServing;
    private double sodiumPerServing;
    private double totalCarbsPerServing;
    private double fiberPerServing;
    private double totalSugarsPerServing;
    private double totalAddedSugarsPerServing;
    private double proteinPerServing;

    public Ingredient(){};

    public String getId() {
        return this.id;
    }

    public void setObjectId(String id) {
        this.id = id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return this.description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getNotes() {
        return this.notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public String getUrl() {
        return this.url;
    }

    public void setUrl(String url) {
        this.url = url;
    }    

    public double getNumberOfContainers() {
        return this.numberOfContainers;
    }

    public void setNumberOfContainers(double numberOfContainers) {
        this.numberOfContainers = numberOfContainers;
    }

    public double getServingsPerContainer() {
        return this.servingsPerContainer;
    }

    public void setServingsPerContainer(double servingsPerContainer) {
        this.servingsPerContainer = servingsPerContainer;
    }

    public double getGramsPerServing() {
        return this.gramsPerServing;
    }

    public void setGramsPerServing(double gramsPerServing) {
        this.gramsPerServing = gramsPerServing;
    }

    public double getCaloriesPerServing() {
        return this.caloriesPerServing;
    }

    public void setCaloriesPerServing(double caloriesPerServing) {
        this.caloriesPerServing = caloriesPerServing;
    }

    public double getTotalFatPerServing() {
        return this.totalFatPerServing;
    }

    public void setTotalFatPerServing(double totalFatPerServing) {
        this.totalFatPerServing = totalFatPerServing;
    }

    public double getSaturatedFatPerServing() {
        return this.saturatedFatPerServing;
    }

    public void setSaturatedFatPerServing(double saturatedFatPerServing) {
        this.saturatedFatPerServing = saturatedFatPerServing;
    }

    public double getTransFatPerServing() {
        return this.transFatPerServing;
    }

    public void setTransFatPerServing(double transFatPerServing) {
        this.transFatPerServing = transFatPerServing;
    }

    public double getCholesterolPerServing() {
        return this.cholesterolPerServing;
    }

    public void setCholesterolPerServing(double cholesterolPerServing) {
        this.cholesterolPerServing = cholesterolPerServing;
    }

    public double getSodiumPerServing() {
        return this.sodiumPerServing;
    }

    public void setSodiumPerServing(double sodiumPerServing) {
        this.sodiumPerServing = sodiumPerServing;
    }

    public double getTotalCarbsPerServing() {
        return this.totalCarbsPerServing;
    }

    public void setTotalCarbsPerServing(double totalCarbsPerServing) {
        this.totalCarbsPerServing = totalCarbsPerServing;
    }

    public double getFiberPerServing() {
        return this.fiberPerServing;
    }

    public void setFiberPerServing(double fiberPerServing) {
        this.fiberPerServing = fiberPerServing;
    }

    public double getTotalSugarsPerServing() {
        return this.totalSugarsPerServing;
    }

    public void setTotalSugarsPerServing(double totalSugarsPerServing) {
        this.totalSugarsPerServing = totalSugarsPerServing;
    }

    public double getTotalAddedSugarsPerServing() {
        return this.totalAddedSugarsPerServing;
    }

    public void setTotalAddedSugarsPerServing(double totalAddedSugarsPerServing) {
        this.totalAddedSugarsPerServing = totalAddedSugarsPerServing;
    }

    public double getProteinPerServing() {
        return this.proteinPerServing;
    }

    public void setProteinPerServing(double proteinPerServing) {
        this.proteinPerServing = proteinPerServing;
    }
    
}
